    private void przeliczDane(){
      
       DecimalFormat df2 = new DecimalFormat( "#,###,###,##0.00" );
       SimpleDateFormat dt1 = new SimpleDateFormat("yyyy-MM-dd");
        
       this.setCursor( Cursor.getPredefinedCursor( Cursor.WAIT_CURSOR ) );  
        
       podsumowanieKontraktow  = napServ.podajPodsumowanieKontraktow( jTextDzien.getText() );
       
       
        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();

        
        TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(jTable1.getModel());
        jTable1.setRowSorter(sorter);
        
        tm.getDataVector().removeAllElements();
        tm.fireTableDataChanged();
        
        for(NapPodsumowanieKontraktowDTO sp : podsumowanieKontraktow) 
        {
  
            Object[] dok = { sp.getSk(), sp.getObPelnyKod(), sp.getMiasto(), sp.getOpis(), dt1.format( sp.getDataZakonczenia() ) 
                    , sp.getKontrakt(), sp.getKwotaMiesieczna(), sp.getIlMiesiecy() };
            //dokLst[i] = dok;
            tm.addRow(dok);
            
        }
        
        
        TableColumnModel tcm = jTable1.getColumnModel();
        tcm.getColumn(6).setCellRenderer( new NumberRenderer( df2 ) );
        
        
         this.setCursor( Cursor.getDefaultCursor() );
       
        
    }

